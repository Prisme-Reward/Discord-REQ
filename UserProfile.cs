        {
            var Req = (HttpWebRequest)WebRequest.Create($"https://discordapp.com/api/v6/users/@me/settings");
            var Data = Encoding.ASCII.GetBytes("{\"status\":\"idle\"}");

            Req.Method = "PATCH";
            Req.UserAgent = UserAgent;
            Req.ContentType = "application/json";
            Req.Headers.Add("authorization", Token);
            Req.ContentLength = Data.Length;

            using (var stream = Req.GetRequestStream()) { stream.Write(Data, 0, Data.Length); }
            var Response = (HttpWebResponse)Req.GetResponse();
            var ResponseInString = new StreamReader(Response.GetResponseStream()).ReadToEnd();
            return ResponseInString;
        }

